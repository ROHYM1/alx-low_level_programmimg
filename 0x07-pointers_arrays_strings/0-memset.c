/**
 * *_memset -  memory with a constant byte
 * @s: memory space to be filled
 * @b: byte to fill with
 * @n: number of spaces to fill
 *
 * Return: pointer to memory area s
 */

char *_memset(char *s, char b, unsigned int n);

{
	int b;
	unsigned int i, n;



		i = 0;
	while (i <= n)

	{

		s[i] = b;
		i++;

	}

	return (s);

}
